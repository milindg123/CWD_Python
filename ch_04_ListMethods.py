# l1.insert()
L1.insert(1,56)       # Insert 56 at the index of 1 in the list
print(L1)

# L1.pop()

L1.pop(2)             # remove element at the index 2
print(L1)

# L1.remove()

L1.remove(1)          # remove 1 from the list
print(L1)