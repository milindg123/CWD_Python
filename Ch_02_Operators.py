# Arithmetic Operators --->( +, - , * , / )

print("The value of 3+5 is ", 3+5)
print("The value of 3-5 is ", 3-5)
print("The value of 3*5 is ", 3*5)
print("The value of 3/5 is ", 3/5)


# Assignment Operators  --> ( =, -=, +=, *=, /= )

c = 34
c += 12
c -= 12
c *= 12
c /= 12
print(c)


# Comparison Operators  ----> (==, > , <, >=, <=, !=)

d = (4>7)
print(d)            # comparison operators return boolean output i.e. True or false


e = (14 != 8)
print(e)


